    public int evalua(int valor)
    {
        if (String.valueOf(jCBTipoEmple.getSelectedItem()).equals("Gerente"))
        {
            valor = 2;
        } else if (String.valueOf(jCBTipoEmple.getSelectedItem()).equals("Vendedor"))
        {
            valor = 3;
        }
        return valor;
    }

