    private void ModificarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_ModificarActionPerformed
    {//GEN-HEADEREND:event_ModificarActionPerformed
        try
        {
            if (Mensaje.pregunta(this, "¿Esta seguro de modificar este empleado?") == JOptionPane.YES_OPTION)
            {
                int filaCelda = jTConsulta.getSelectedRow();
                if (filaCelda == -1)
                {
                    Mensaje.error(this, "Selecciona un registro");
                    flag = true;
                } else
                {
                    int cont = 0;
                    jBCancelarActionPerformed(null);

                    String cod = (String) jTConsulta.getValueAt(filaCelda, 0);
                    columnaMap3 = q.Seleccion(con, "*", "empleados", "id_empleado = " + "'" + cod.substring(1) + "'", false);

                    //Mostrar los datos para el modificar
                    jTFID.setText((String) columnaMap3.get(0));

                    cargaComboSucursal();
                    while (cont <= columnaMap1.size())
                    {
                        if (columnaMap1.get(cont).equals(((String) columnaMap3.get(1)).substring(1)))
                        {
                            jCBSucursal.setSelectedIndex(cont);
                            break;
                        } else
                        {
                            cont++;
                        }
                    }

                    jTFNombre.setText(((String) columnaMap3.get(2)).substring(1));
                    jTFAPaterno.setText(((String) columnaMap3.get(3)).substring(1));
                    jTFAMaterno.setText(((String) columnaMap3.get(4)).substring(1));
                    switch ((String) columnaMap3.get(5))
                    {
                        case "M":
                            jCBSexo.setSelectedIndex(0);
                            break;

                        case "F":
                            jCBSexo.setSelectedIndex(1);
                            break;

                        default:
                            System.out.println("Error de opcion");
                    }
                    jTFFechaNac.setText(((String) columnaMap3.get(6)).substring(1));
                    jTFCurp.setText(((String) columnaMap3.get(7)).substring(1));
                    jTFTelefono.setText(((String) columnaMap3.get(8)).substring(1));
                    switch ((String) columnaMap3.get(9))
                    {
                        case "2":
                            jCBTipoEmple.setSelectedIndex(0);
                            break;

                        case "3":
                            jCBTipoEmple.setSelectedIndex(1);
                            break;

                        case "4":
                            jCBTipoEmple.setSelectedIndex(2);
                            break;

                        default:
                            System.out.println("Error de opcion");
                    }
                    jTFCorreo.setText(((String) columnaMap3.get(10)).substring(1));
                    jPFContra.setText(((String) columnaMap3.get(11)).substring(1));
                    jTFFechaAlta.setText(((String) columnaMap3.get(12)).substring(1));

                    flag = false;
                }
            }
        } catch (Exception e)
        {
        }
    }//GEN-LAST:event_ModificarActionPerformed

