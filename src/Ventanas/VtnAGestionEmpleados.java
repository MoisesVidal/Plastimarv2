    private void mostrar()
    {
        DefaultTableModel model = new DefaultTableModel();
        q = new Querys();
        int flag = 0;

        try
        {
            columnaMap2 = q.Seleccion(con, "id_empleado, id_sucursal, nombre, apellido_paterno, apellido_materno", "empleados", "", false);
            model.setColumnIdentifiers(new Object[]
            {
                "ID Empleado", "ID Sucursal", "Nombre", "A. Paterno", "A. Materno"
            });
            while (flag != columnaMap2.size())
            {
                model.addRow(new Object[]
                {
                    columnaMap2.get(flag),
                    columnaMap2.get(flag + 1),
                    columnaMap2.get(flag + 2),
                    columnaMap2.get(flag + 3),
                    columnaMap2.get(flag + 4)

                });
                flag = flag + 5;
            }

            jTConsulta.setModel(model);
            jTConsulta.setValueAt(" " + columnaMap2.get(0), 0, 0);
        } catch (Exception e)
        {
            System.out.println("error --> " + e);
        }
    }

    private void generaID()
    {
        q = new Querys();
        GeneraCodigo gen = new GeneraCodigo();
        String idSucursal;
        try
        {
            columnaMap1 = q.Seleccion(con, "MAX(id_empleado)", "empleados", "", true);
            if (columnaMap1.get(0).equals("null"))
            {
                jTFID.setText("E01");
            } else
            {
                mostrar();
                idSucursal = (String) columnaMap1.get(0);
                gen.generar(Integer.parseInt(idSucursal.substring(1)));
                jTFID.setText("E" + gen.serie());
                jTFID.setEnabled(false);
                CtrlInterfaz.selecciona(jTFNombre);
            }
        } catch (Exception e)
        {
            System.out.println("Error: exception ->" + e);
        }
    }

    private void cargaComboSucursal()
    {
        int contador = 0;
        jCBSucursal.removeAllItems();
        try
        {
            q = new Querys();

            columnaMap1 = q.Seleccion(con, "id_sucursal", "sucursal", "", true);
            while (columnaMap1.size() > contador)
            {
                jCBSucursal.addItem((String) columnaMap1.get(contador));
                contador++;
            }

        } catch (Exception e)
        {
        }
    }

